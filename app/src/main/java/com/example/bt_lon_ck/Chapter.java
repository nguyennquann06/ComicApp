package com.example.bt_lon_ck;

public class Chapter {
    private int chapterId;
    private int comicId;
    private int chapterNumber;
    private String chapterTitle;
    private String chapterImage;

    public Chapter() {}

    public int getChapterId() { return chapterId; }
    public void setChapterId(int chapterId) { this.chapterId = chapterId; }
    public int getComicId() { return comicId; }
    public void setComicId(int comicId) { this.comicId = comicId; }
    public int getChapterNumber() { return chapterNumber; }
    public void setChapterNumber(int chapterNumber) { this.chapterNumber = chapterNumber; }
    public String getChapterTitle() { return chapterTitle; }
    public void setChapterTitle(String chapterTitle) { this.chapterTitle = chapterTitle; }
    public String getChapterImage() { return chapterImage; }
    public void setChapterImage(String chapterImage) { this.chapterImage = chapterImage; }
}